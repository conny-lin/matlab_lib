function makefig(G)
%% create figure 
figure1 = figure('Color',[1 1 1]); % Create figure with white background

x = 1;
axes1 = axes('Parent',figure1,...
    'XTickLabel','',...
    'FontName',G.FontName);
%
hold(axes1,'all');
errorbar1 = errorbar(G.Y,G.E,...
    'Marker','.',...
    'LineWidth',1);
for i = 1:size(G.Y,2);
    set(errorbar1(i),...
        'DisplayName', G.legend{i,1},...
        'Color',G.Color(i,:)); 
end
ylim(axes1,[G.Ymin(x) G.Ymax(x)]);
xlim(axes1,[G.Xmin(x) G.Xmax(x)]);
ylabel(G.Ylabel,'FontName',G.FontName);
xlabel(G.Xlabel,'FontName',G.FontName);


legend(axes1,'show');
set(legend,...
    'Location','NorthEast',...
    'EdgeColor',[1 1 1],...
    'YColor',[1 1 1],...
    'XColor',[1 1 1],...
    'TickDir','in',...
    'LineWidth',1);


%% annotation: N
N = size(G.Y,2);
s = 'N=%d';
text = sprintf(s,N);
annotation(figure1,...
    'textbox',G.Gp(6,1:4),...
    'String',{text},...
    'FontSize',10,...
    'FontName',G.FontName,...
    'FitBoxToText','on',...
    'EdgeColor','none');

%% annotation: experiment name
annotation(figure1,...
    'textbox',G.Gp(7,1:4),...
    'String',{G.expname},...
    'FontSize',10,...
    'FontName',G.FontName,...
    'FitBoxToText','on',...
    'EdgeColor','none');
end



